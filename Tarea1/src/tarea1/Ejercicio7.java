//Programa que imprime numeros pares del 1 al 100
package tarea1;
public class Ejercicio7 {
    public static void main(String args[]) {
        //Creamos el for directamente, pediremos saltos sencillos y multiplicaremos por 2
        for(int i=1;i<=50;i=i+1){
            System.out.println(2*i);
        }
    }
}
